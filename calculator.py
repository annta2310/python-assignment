# Assign values to variables
a = 10
b = 5

# Addition
sum = a + b
print('Sum: ', sum)

# Subtraction
difference = a - b
print('Difference: ', difference)

# Multiplication
product = a * b
print('Product: ', product)

# Floor division
floor_division = a // b
print('Floor Division: ', floor_division)

# Modulus
modulus = a % b
print('Modulus: ', modulus)

# Exponentiation
exponentiation = a ** b
print('Exponentiation: ', exponentiation)